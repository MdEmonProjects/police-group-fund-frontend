// Helper function to get all policy slugs for routing
export const getAllPolicySlugs = () => {
  return Object.keys(policyContent).map(key => ({
    params: { slug: policyContent[key].slug }
  }));
};

// Helper function to get policy by slug
export const getPolicyBySlug = (slug) => {
  const policy = Object.values(policyContent).find(policy => policy.slug === slug);
  return policy || null;
};

